    public void validaNumero(KeyEvent evt) {
        if (evt.getKeyCode() > 57) {
            JTextField campo = (JTextField) evt.getSource();
            campo.setText(removeUltimo(campo.getText()));
        }
    }

    private String removeUltimo(String palavra) {
        int totalDeCaracteres = palavra.length();
        String retorno = palavra.substring(0, totalDeCaracteres - 1);
        return retorno;
    }

    public void validaString(KeyEvent evt) {
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            JTextField campo = (JTextField) evt.getSource();
            campo.setText(removeUltimo(campo.getText()));
        }
    }
