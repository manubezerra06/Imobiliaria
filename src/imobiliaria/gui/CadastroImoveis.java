    private void jTableImoveisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableImoveisMouseClicked
        if (evt.getClickCount() >= 2) {
            int linhaSelecionada = jTableImoveis.getSelectedRow();

            if (linhaSelecionada == -1) {
                return;
            }
            else{
            Imoveis2Table model = (Imoveis2Table) jTableImoveis.getModel();
            Imoveis ImovelSelecionado = model.getSelectedImoveis(linhaSelecionada);
            idDoImovelCarregado = ImovelSelecionado.getCodigo();
            jtfNome.setText(ImovelSelecionado.getProprietario());
            jtfEndereco.setText(ImovelSelecionado.getEndereco());
            jtfNumero.setText(ImovelSelecionado.getNumero());
            jtfBairro.setText(ImovelSelecionado.getBairro());
            jtfCidade.setText(ImovelSelecionado.getCidade());
            jtfEstado.setText(ImovelSelecionado.getEstado());
            jtfPais.setText(ImovelSelecionado.getPais());
            jtfTel.setText(ImovelSelecionado.getTelefone());
            jtfValorMin.setText(ImovelSelecionado.getValorminimo());
            jtfValorMax.setText(ImovelSelecionado.getValormaximo());
            }
    }//GEN-LAST:event_jTableImoveisMouseClicked
 }
