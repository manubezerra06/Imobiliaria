    private void jmSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmSalvarActionPerformed
       if(!validaSeVazio()){
       JOptionPane.showMessageDialog(this, "Campos com valores nulos ou invalidos!!!", 
       "ERRO", JOptionPane.ERROR_MESSAGE);
       return;
       }
     
       Imoveis imovel = new Imoveis();
       if (idDoImovelCarregado != 0) 
            imovel.setCodigo(idDoImovelCarregado);
            //idDoImovelCarregado = 0;
            imovel.setEndereco(jtfEndereco.getText());
            imovel.setNumero(jtfNumero.getText());
            imovel.setBairro(jtfBairro.getText());
            imovel.setCidade(jtfCidade.getText());
            imovel.setEstado(jtfEstado.getText());
            imovel.setPais(jtfPais.getText());
            imovel.setProprietario(jtfNome.getText());
            imovel.setTelefone(jtfTel.getText());
            imovel.setValormaximo(jtfValorMax.getText());
            imovel.setValorminimo(jtfValorMin.getText());
       
            ImovelControle.insereImovel(imovel);
            limparFormulario();
            ArrayList<Imoveis> listaImoveis = ImovelControle.listar();
            Imoveis2Table model = (Imoveis2Table) jTableImoveis.getModel();
            model.setImoveis(listaImoveis);
      
    
       
    }//GEN-LAST:event_jmSalvarActionPerformed

    private boolean validaSeVazio(){
    if (jtfEndereco.getText().isEmpty()||       
        jtfNumero.getText().isEmpty()||
        jtfBairro.getText().isEmpty()||
        jtfCidade.getText().isEmpty()||
        jtfEstado.getText().isEmpty()||
        jtfPais.getText().isEmpty()||
        jtfNome.getText().isEmpty()||
        jtfTel.getText().isEmpty()||
        jtfValorMin.getText().isEmpty()||
        jtfValorMax.getText().isEmpty())
        return false;
    
     return true;
  }  
