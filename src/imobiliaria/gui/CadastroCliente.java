    private boolean validaFormulario() {
        boolean formularioValido = true;

        if (jTextFieldNome.getText().isEmpty()) {
            jTextFieldNome.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldNome.setBackground(Color.white);
        }
        if (jTextFieldCPF.getText().isEmpty()) {
            jTextFieldCPF.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldCPF.setBackground(Color.white);
        }
        if (jTextFieldEndereco.getText().isEmpty()) {
            jTextFieldEndereco.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldEndereco.setBackground(Color.white);
        }
        if (jTextFieldBairro.getText().isEmpty()) {
            jTextFieldBairro.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldBairro.setBackground(Color.white);
        }
        if (jTextFieldCidade.getText().isEmpty()) {
            jTextFieldCidade.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldCidade.setBackground(Color.white);
        }
        if (jTextFieldEstado.getText().isEmpty()) {
            jTextFieldEstado.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldEstado.setBackground(Color.white);

        }
        if (jTextFieldCEP.getText().isEmpty()) {
            jTextFieldCEP.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldCEP.setBackground(Color.white);
        }

        if (jTextFieldTelefone.getText().isEmpty()) {
            jTextFieldTelefone.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldTelefone.setBackground(Color.white);
        }

        if (jTextFieldEmail.getText().isEmpty() || !jTextFieldEmail.getText().matches("[a-zA-Z0-9]+[a-zA-Z0-9_.-]+@{1}[a-zA-Z0-9_.-]*\\.+[a-z]{2,4}")) {
            jTextFieldEmail.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jTextFieldEmail.setBackground(Color.white);
        }
        if (jComboBoxRenda.getSelectedIndex() == 0) {
            jComboBoxRenda.setBackground(Color.RED);
            formularioValido = false;
        } else {
            jComboBoxRenda.setBackground(null);
        }

        return  formularioValido;
    }

