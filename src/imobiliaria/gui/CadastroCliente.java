    private void jTableClientesCadastradosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClientesCadastradosMouseClicked
        if (evt.getClickCount() >= 2) {
            int linhaSelecionada = jTableClientesCadastrados.getSelectedRow();

            if (linhaSelecionada == -1) {
                return;
            }
            Clientes2Table model = (Clientes2Table) jTableClientesCadastrados.getModel();
            Clientes clienteSelecionado = model.getSelectedCliente(linhaSelecionada);
            codigoSelecionado = clienteSelecionado.getCodigo();
            jTextFieldNome.setText(clienteSelecionado.getNome());
            jTextFieldCPF.setText(clienteSelecionado.getCpf());
            if (clienteSelecionado.isMasculino()) {
                jRadioButtonMasculino.setSelected(true);
                jRadioButtonFeminino.setSelected(false);
            } else {
                jRadioButtonMasculino.setSelected(false);
                jRadioButtonFeminino.setSelected(true);
            }
            jTextFieldEndereco.setText(clienteSelecionado.getEndereco());
            jTextFieldBairro.setText(clienteSelecionado.getBairro());
            jTextFieldCidade.setText(clienteSelecionado.getCidade());
            jTextFieldEstado.setText(clienteSelecionado.getEstado());
            jTextFieldCEP.setText(clienteSelecionado.getCep());
            jTextFieldTelefone.setText(clienteSelecionado.getTelefone());
            jTextFieldEmail.setText(clienteSelecionado.getEmail());
            jComboBoxRenda.setSelectedItem(clienteSelecionado.getRenda());
            if (clienteSelecionado.getVenda() == true) {
                jCheckBoxVenda.setSelected(true);
            } else {
                jCheckBoxVenda.setSelected(false);
            }
            if (clienteSelecionado.getLocacao() == true) {
                jCheckBoxLocacao.setSelected(true);
            } else {
                jCheckBoxLocacao.setSelected(false);
            }
        }

    }//GEN-LAST:event_jTableClientesCadastradosMouseClicked

